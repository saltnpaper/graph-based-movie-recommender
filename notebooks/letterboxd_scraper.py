# ─── HELPERS ──────────────────────────────────────────────────────────────────

def polite_sleep():
    time.sleep(random.uniform(MIN_SLEEP, MAX_SLEEP))


def long_pause():
    duration = random.uniform(*LONG_PAUSE_RANGE)
    print(f"  [long pause] sleeping {duration:.0f}s...")
    time.sleep(duration)


