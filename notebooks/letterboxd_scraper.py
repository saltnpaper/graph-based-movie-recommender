def make_driver() -> uc.Chrome:
    opts = uc.ChromeOptions()
    opts.add_argument(f"--user-data-dir={PROFILE_DIR}")
    opts.add_argument("--profile-directory=Default")
    opts.add_argument("--window-size=1400,900")
    opts.add_argument("--lang=en-US")
    driver = uc.Chrome(
    options=opts,
    use_subprocess=True,
    version_main=145  # Explicitly set to 122 for 122.0.6261.57
)
    driver.set_page_load_timeout(PAGE_TIMEOUT)
    return driver


def is_blocked(driver) -> bool:
    t   = (driver.title or "").lower()
    src = (driver.page_source or "").lower()
    return (
        "just a moment" in t
        or "cf-browser-verification" in src
        or ("captcha" in src and "cloudflare" in src)
        or "enable javascript" in src
    )


def maybe_accept_cookies(driver):
    for css in [
        "button#onetrust-accept-btn-handler",
        "button[aria-label='Accept cookies']",
        "button[title='Accept']",
    ]:
        btns = driver.find_elements(By.CSS_SELECTOR, css)
        if btns:
            try:
                btns[0].click()
                return
            except WebDriverException:
                pass


