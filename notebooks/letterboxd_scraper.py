def run_production(
    input_csv:  str = "films_500.csv",
    output_csv: str = "edges_500films.csv",
):
    # Load slug list
    slugs_df = pd.read_csv(input_csv)
    slugs    = slugs_df["film_slug"].dropna().str.strip().tolist()
    print(f"Loaded {len(slugs)} slugs from {input_csv}")

    # Resume support — skip already-finished slugs
    if os.path.exists(output_csv):
        done_df    = pd.read_csv(output_csv)
        done_slugs = set(done_df["film_slug"].unique())
        slugs      = [s for s in slugs if s not in done_slugs]
        all_rows   = done_df.to_dict("records")
        print(f"Resuming: {len(done_slugs)} done, {len(slugs)} remaining")
    else:
        all_rows = []

    driver = make_driver()
    try:
        warmup_manual(driver)


        for idx, slug in enumerate(slugs, start=1):
            print(f"\n[{idx}/{len(slugs)}] {slug}")

            try:
                rows = scrape_film_users(driver, slug)
                n    = len(set(r["user_id"] for r in rows))
                print(f"  [done] {n} users collected")
                all_rows.extend(rows)
            except Exception as e:
                print(f"  [failed] {e}")

            # Checkpoint every 10 films
            if idx % 10 == 0:
                edges_df = pd.DataFrame(all_rows).drop_duplicates(
                    subset=["user_id", "film_slug"]
                )
                edges_df.to_csv(output_csv, index=False)
                print(f"  [checkpoint] {len(edges_df)} edges saved")

            polite_sleep()
            if idx % LONG_PAUSE_EVERY == 0:
                long_pause()

    finally:
        edges_df = pd.DataFrame(all_rows).drop_duplicates(subset=["user_id", "film_slug"])
        edges_df.to_csv(output_csv, index=False)
        print(f"\n[DONE] {len(edges_df)} edges → {output_csv}")
        driver.quit()


