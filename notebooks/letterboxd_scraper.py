def get_session_from_driver(driver) -> requests.Session:
    """
    Copy the browser's Cloudflare-cleared cookies into a requests.Session
    so we can make fast AJAX calls without the browser."""
    session = requests.Session()
    for cookie in driver.get_cookies():
        session.cookies.set(cookie["name"], cookie["value"])
    session.headers.update({
        "User-Agent":        driver.execute_script("return navigator.userAgent"),
        "X-Requested-With":  "XMLHttpRequest",
        "Referer":           BASE,
        "Accept":            "text/html, */*; q=0.01",
        "Accept-Language":   "en-US,en;q=0.9",
    })
    print("Session built with browser cookies.")
    return session

def wait_for_clearance(driver, timeout=120):
    deadline = time.time() + timeout
    while time.time() < deadline:
        if not is_blocked(driver):
            return True
        time.sleep(1.5)
    return False

def scrape_film_users(driver, slug: str, target: int = TARGET_USERS_PER_FILM) -> list[dict]:
    """
    Scrape reviewers from /film/{slug}/reviews/ pages.
    Each page has ~12 reviewers. Paginate until target is reached.
    """
    all_rows = []
    seen_users = set()
    page = 1
    max_pages = 20  # safety limit

    while len(seen_users) < target and page <= max_pages:
        if page == 1:
            url = f"{BASE}/film/{slug}/reviews/"
        else:
            url = f"{BASE}/film/{slug}/reviews/page/{page}/"

        driver.get(url)

        if not wait_for_clearance(driver, timeout=60):
            print(f"  [blocked] on page {page}")
            break

        # Check for 404 / film not found
        if "not found" in driver.title.lower() or "sorry, we can't find" in driver.page_source.lower():
            print(f"  [404] film not found on Letterboxd - bad slug")
            return []  # skip this film entirely
        # Wait for reviews to load
        try:
            WebDriverWait(driver, 20).until(
                EC.presence_of_element_located((By.CSS_SELECTOR, "a.avatar"))
            )
        except TimeoutException:
            print(f"  [empty] no reviews on page {page}")
            break

        # Parse usernames from avatars
        avatars = driver.find_elements(By.CSS_SELECTOR, "a.avatar img[alt]")
        if not avatars:
            print(f"  [done] no more pages")
            break

        new_users = 0
        for img in avatars:
            user = img.get_attribute("alt")
            if user and user not in seen_users:
                seen_users.add(user)
                all_rows.append({
                    "user_id": user.strip(),
                    "film_slug": slug,
                    "film_display": slug,  # we can enhance this later if needed
                })
                new_users += 1

        print(f"  [page {page}] +{new_users} new | total: {len(seen_users)}")

        if new_users == 0:  # no new users on this page, we've hit duplicates or the end
            print(f"  [done] no new users on page {page}")
            break

        page += 1
        time.sleep(random.uniform(1.5, 3.0))

    print(f"  [total] {len(seen_users)} unique users")
    return all_rows


